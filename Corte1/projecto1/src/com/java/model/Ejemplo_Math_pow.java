package com.java.model;

public class Ejemplo_Math_pow {

    public static void main(String[] args) {
        // Declarar variable
        double base = 5;
        double exponente = 3;
    
        // Calcular potencia usando Math.pow
        double resultado = Math.pow(base, exponente);

        System.out.println("resultado : " + resultado);   
    }
}
